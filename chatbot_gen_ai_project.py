def define_term(term):
    term = term.strip()
    if not term:
        return "Enter a term."
    low = term.lower()
    if low in GLOSSARY:
        return f"**{term}**: {GLOSSARY[low]}"
    try:
        return new_llm().invoke(f"Explain the BI term '{term}' briefly.").content
    except Exception:
        return "Definition service unavailable."

